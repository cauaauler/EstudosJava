package Exemplos;

public class Exemplo3 {

	public static void main(String[] args) {
		
		int i = 9;
		
		while(i>=0) {
			System.out.print(i + " ");
			i--;
		}
		
		System.out.println();
		System.out.println("-------------------");
		
		for (int j = 0; j < 10; j++) {
			System.out.print(j + " ");
			
		}

	}

}
