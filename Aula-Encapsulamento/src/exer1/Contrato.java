package exer1;

public class Contrato {

	private double valor;
	private Empresa contratante;
	private Empresa contratado;
	
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public Empresa getContratante() {
		return contratante;
	}
	public void setContratante(Empresa contratante) {
		this.contratante = contratante;
	}
	public Empresa getContratado() {
		return contratado;
	}
	public void setContratado(Empresa contratado) {
		this.contratado = contratado;
	}
	
	
}
